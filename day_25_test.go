func part1(input string) int {
	const ROWS = 7
	const COLS = 5

	var locks [][5]int
	var keys [][5]int
	for _, rows := range strings.Split(input, "\n\n") {
		var grid [5]int
		var isLock bool
		for y, row := range strings.Split(rows, "\n") {
			for x, col := range row {
				if y == 0 && col == '#' {
					isLock = true
				}
				if col == '#' {
					grid[x]++
				}
			}
		}
		if isLock {
			locks = append(locks, grid)
		} else {
			keys = append(keys, grid)
		}
	}

	type Key struct {
		a [5]int
		b [5]int
	}
	cache := make(map[Key]bool)
	for _, lock := range locks {
		for _, key := range keys {
			valid := true
			for i := 0; i < COLS; i++ {
				if lock[i]+key[i] > ROWS {
					valid = false
				}
			}
			if valid {
				cache[Key{lock, key}] = true
			}
		}
	}

	return len(cache)
}

