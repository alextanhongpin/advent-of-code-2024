func part2(input string) int {
	var m [][]string
	var rows, cols int
	for _, row := range scan(input) {
		m = append(m, strings.Split(row, ""))
		rows++
		cols = len(row)
	}

	var total int
	for i := 1; i < rows-1; i++ {
		for j := 1; j < cols-1; j++ {
			if m[i][j] == "A" {
				a := isMAS(m[i][j] + m[i-1][j-1] + m[i+1][j+1])
				b := isMAS(m[i][j] + m[i-1][j+1] + m[i+1][j-1])
				if a && b {
					total++
				}
			}
		}
	}

	return total
}

func isMAS(str string) bool {
	chars := strings.Split(str, "")
	slices.Sort(chars)
	return strings.Join(chars, "") == "AMS"
}

func part1(input string) int {
	m := make([][]string, 0)
	var all []string
	var rows int
	var cols int
	for _, row := range scan(input) {
		rows++
		cols = len(row)

		m = append(m, strings.Split(row, ""))
		all = append(all, row)
		all = append(all, reverse(row))
	}

	for i := 0; i < cols; i++ {
		var col string
		for j := 0; j < rows; j++ {
			col += m[j][i]
		}
		all = append(all, col)
		all = append(all, reverse(col))
	}

	// Diagonals.
	for i := 0; i < rows; i++ {
		for j := 0; j < cols; j++ {
			var ok bool
			if i == 0 || i == rows-1 || j == 0 || j == cols-1 {
				ok = true
			}
			if !ok {
				continue
			}
			row := i
			col := j
			var diag string
			for row < rows && col < cols {
				diag += m[row][col]
				row++
				col++
			}
			row = i
			col = j

			all = append(all, diag)
			all = append(all, reverse(diag))

			row = i
			col = j
			diag = ""
			for row > -1 && row < rows && col > -1 && col < cols {
				diag += m[row][col]
				row--
				col++
			}

			all = append(all, diag)
			all = append(all, reverse(diag))
		}
	}

	re := regexp.MustCompile(`XMAS`)
	var total int
	for _, row := range all {
		matches := re.FindAllStringSubmatch(row, -1)
		total += len(matches)
	}

	return total
}

func reverse(s string) string {
	rs := []rune(s)
	slices.Reverse(rs)
	return string(rs)
}

func scan(input string) iter.Seq2[int, string] {
	return func(yield func(int, string) bool) {
		var i int

		scanner := bufio.NewScanner(strings.NewReader(input))
		for scanner.Scan() {
			if !yield(i, scanner.Text()) {
				break
			}
			i++
		}
	}
}

