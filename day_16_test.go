func part1(input string) int {
	return solve(parse(input)).score()
}

func part2(input string) int {
	grid, start, end := parse(input)
	sol := solve(grid, start, end)

	last := end
	// We skip the last one because it can have multiple paths leading to it.
	end = sol.hist[len(sol.hist)-2]
	dist := sol.steps() - 1

	visited := make(map[cacheKey]int)
	var states []*state
	pq := &PriorityQueue{items: make([]*state, 1)}
	pq.items[0] = &state{start, 1 + 0i, 0, []Point{start}}
	heap.Init(pq)
	for pq.Len() > 0 {
		h := heap.Pop(pq).(*state)
		if h.pos == end {
			states = append(states, h)
			continue
		}
		key := cacheKey{h.pos, h.dir}
		if _, ok := visited[key]; !ok {
			visited[key] = h.score()
		}
		if visited[key] < h.score() {
			continue
		}
		visited[key] = h.score()
		if grid[h.pos] {
			continue
		}

		heap.Push(pq, h.clockwise())
		heap.Push(pq, h.anticlockwise())
		heap.Push(pq, h.move())
	}

	unique := make(map[Point]bool)
	unique[last] = true
	for _, s := range states {
		if s.steps() != dist {
			continue
		}
		for _, p := range s.hist {
			unique[p] = true
		}
	}

	return len(unique)
}

type Point = complex128

func parse(input string) (grid map[Point]bool, start, end Point) {
	grid = make(map[Point]bool)
	for y, row := range strings.Split(input, "\n") {
		for x, ch := range row {
			p := complex(float64(x), float64(y))
			if ch == 'S' {
				start = p
			} else if ch == 'E' {
				end = p
			} else if ch == '#' {
				grid[p] = true
			}
		}
	}
	return
}

func solve(grid map[Point]bool, start, end Point) *state {
	visited := make(map[cacheKey]bool)
	pq := &PriorityQueue{items: make([]*state, 1)}
	pq.items[0] = &state{start, 1 + 0i, 0, []Point{start}}
	heap.Init(pq)
	for pq.Len() > 0 {
		h := heap.Pop(pq).(*state)
		if h.pos == end {
			return h
		}
		key := cacheKey{h.pos, h.dir}
		if visited[key] {
			continue
		}
		visited[key] = true
		if grid[h.pos] {
			continue
		}

		heap.Push(pq, h.clockwise())
		heap.Push(pq, h.anticlockwise())
		heap.Push(pq, h.move())
	}
	return nil
}

type cacheKey struct {
	pos, dir Point
}

type state struct {
	pos  Point
	dir  Point
	rot  int
	hist []Point
}

func (s *state) clone() *state {
	return &state{
		pos:  s.pos,
		dir:  s.dir,
		rot:  s.rot,
		hist: slices.Clone(s.hist),
	}
}

func (s *state) move() *state {
	n := s.clone()
	n.pos = n.pos + n.dir
	n.hist = append(n.hist, n.pos)
	return n
}
func (s *state) anticlockwise() *state {
	n := s.clone()
	n.rot++
	n.dir = n.dir * -1i
	return n
}
func (s *state) clockwise() *state {
	n := s.clone()
	n.rot++
	n.dir = n.dir * 1i
	return n
}

func (s *state) steps() int {
	return len(s.hist) - 1 // Exclude the start Point.
}

func (s *state) score() int {
	return s.steps() + s.rot*1000
}

// A PriorityQueue implements heap.Interface and holds Items.
type PriorityQueue struct {
	items []*state
}

func (pq PriorityQueue) Len() int { return len(pq.items) }

func (pq PriorityQueue) Less(i, j int) bool {
	// We want Pop to give us the highest, not lowest, priority so we use greater than here.
	return pq.items[i].score() < pq.items[j].score()
}

func (pq PriorityQueue) Swap(i, j int) {
	pq.items[i], pq.items[j] = pq.items[j], pq.items[i]
}

func (pq *PriorityQueue) Push(x any) {
	item := x.(*state)
	pq.items = append(pq.items, item)
}

func (pq *PriorityQueue) Pop() any {
	old := pq.items
	n := len(old)
	item := old[n-1]
	old[n-1] = nil // don't stop the GC from reclaiming the item eventually
	pq.items = old[0 : n-1]
	return item
}

