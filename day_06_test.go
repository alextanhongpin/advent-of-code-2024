func parseGrid(input string) (start complex128, grid map[complex128]rune) {
	grid = make(map[complex128]rune)
	for i, row := range strings.Split(input, "\n") {
		for j, col := range row {
			pos := complex(float64(j), float64(i))
			if col == '^' {
				start = pos
				grid[pos] = '.' // replace start with empty space
			} else {
				grid[pos] = col
			}
		}
	}

	return start, grid
}

func getVisited(start complex128, grid map[complex128]rune) map[complex128]bool {
	visit := make(map[complex128]bool)
	visit[start] = true

	dir := complex(0, -1)
	for {
		next := start + dir
		ch, ok := grid[next]
		if !ok {
			break
		}
		if ch == '#' {
			// Turn right
			dir *= complex(0, 1)
			continue
		}
		// Move forward
		start = next

		// Mark as visited
		visit[next] = true
	}

	return visit
}

func checkDuplicate(start complex128, grid map[complex128]rune) bool {
	type step struct {
		pos complex128
		dir complex128
	}

	cache := make(map[step]bool)
	dir := complex(0, -1)
	for {
		next := start + dir
		ch, ok := grid[next]
		if !ok {
			break
		}

		if ch == '#' {
			dir *= complex(0, 1)
			continue
		}

		start = next
		if cache[step{start, dir}] {
			// Found a loop
			return true
		}

		cache[step{start, dir}] = true
	}

	return false
}

func part1(input string) int {
	start, grid := parseGrid(input)
	return len(getVisited(start, grid))
}

func part2(input string) int {
	start, grid := parseGrid(input)
	visited := getVisited(start, grid)

	// For every visited node, try to replace it with a wall and check if it
	// creates a loop.
	var total int
	for k := range visited {
		gridClone := maps.Clone(grid)
		gridClone[k] = '#'
		if checkDuplicate(start, gridClone) {
			total++
		}
	}

	return total
}

