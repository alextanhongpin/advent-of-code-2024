var pos = []complex128{
	0 - 1i,  // up
	0 + 1i,  // down
	-1 + 0i, // left
	1 + 0i,  // right
}

type Batch struct {
	flower string
	pos    []complex128
}

func part1(input string) int {
	flowers := make(map[complex128]string)
	for y, row := range strings.Split(input, "\n") {
		for x, col := range strings.Split(row, "") {
			flowers[complex(float64(x), float64(y))] = col
		}
	}

	var total int
	visited := make(map[complex128]bool)
	for p, f := range flowers {
		if visited[p] {
			continue
		}
		area := make(map[complex128]bool)
		next := []complex128{p}
		for len(next) > 0 {
			curr := next[0]
			next = next[1:]
			if flowers[curr] != f {
				continue
			}
			if area[curr] {
				continue
			}
			area[curr] = true
			for _, d := range pos {
				next = append(next, curr+d)
			}
		}

		peri := make(map[complex128]bool)
		for a := range area {
			for _, d := range pos {
				if flowers[a+d] != f {
					peri[a+d/2] = true
				}
			}
		}

		total += len(peri) * len(area)
		for p := range area {
			visited[p] = true
		}
	}

	return total
}

func part2(input string) int {
	flowers := make(map[complex128]string)
	for y, row := range strings.Split(input, "\n") {
		for x, col := range strings.Split(row, "") {
			flowers[complex(float64(x), float64(y))] = col
		}
	}

	var total int
	visited := make(map[complex128]bool)
	for p, f := range flowers {
		if visited[p] {
			continue
		}
		area := make(map[complex128]bool)
		next := []complex128{p}
		for len(next) > 0 {
			curr := next[0]
			next = next[1:]
			if flowers[curr] != f {
				continue
			}
			if area[curr] {
				continue
			}
			area[curr] = true
			for _, d := range pos {
				next = append(next, curr+d)
			}
		}

		peri := make(map[complex128]bool)
		for a := range area {
			for _, d := range pos {
				if flowers[a+d] != f {
					peri[a+d/4] = true // Add 0.25 to avoid overlapping.
				}
			}
		}

		// Trim perimeter by moving left, right, top and bottom.
		checked := make(map[complex128]bool)
		for k := range peri {
			if checked[k] {
				continue
			}
			checked[k] = true

			for _, p := range pos {
				next := k + p
				for peri[next] {
					delete(peri, next)
					checked[next] = true
					next += p
				}
			}
		}

		total += len(peri) * len(area)
		for p := range area {
			visited[p] = true
		}
	}

	return total
}

