var movesByDir = map[rune]complex128{
	'^': -1i,
	'v': 1i,
	'<': -1,
	'>': 1,
}

func robot(grid map[complex128]string) complex128 {
	for p, v := range grid {
		if v == "@" {
			return p
		}
	}
	panic("no robot")
}

func part1(input string) int {
	grid, moves, robot := parse(input)

	for _, dir := range moves {
		move := movesByDir[dir]
		switch grid[robot+move] {
		case '#':
		case '.':
			robot += move
		case 'O':
			next := robot + move
			for grid[next] == 'O' {
				next += move
			}
			if grid[next] == '#' {
				continue
			}
			robot += move
			grid[next] = 'O'
			grid[robot] = '.'
		}
	}

	var total int
	for pos, tile := range grid {
		if tile != 'O' {
			continue
		}
		x, y := int(real(pos)), int(imag(pos))
		total += x + y*100
	}

	return total
}

func parse(input string) (grid map[complex128]rune, moves string, robot complex128) {
	parts := strings.Split(input, "\n\n")
	grid = make(map[complex128]rune)
	var y int

	scanner := bufio.NewScanner(strings.NewReader(parts[0]))
	for scanner.Scan() {
		text := scanner.Text()
		for x, ch := range []rune(text) {
			p := complex(float64(x), float64(y))
			if ch == '@' {
				robot = p
				ch = '.'
			}
			grid[p] = ch
		}
		y++
	}

	moves = strings.Join(strings.Split(parts[1], "\n"), "")
	return
}

func part2(input string) int {
	compactGrid, moves, robot := parse(input)
	robot = complex(real(robot)*2, imag(robot))
	grid := make(map[complex128]rune)
	for p, v := range compactGrid {
		x := real(p) * 2
		y := imag(p)

		grid[complex(x, y)] = v
		grid[complex(x+1, y)] = v
		if v == 'O' {
			grid[complex(x, y)] = '['
			grid[complex(x+1, y)] = ']'
		}
	}

	for _, dir := range moves {
		move := movesByDir[dir]
		next := robot + move
		switch tile := grid[next]; tile {
		case '#':
		case '.':
			robot = next
		case '[', ']':
			switch dir {
			case '<':
				steps := 0
				ahead := next
				for grid[ahead] == ']' {
					ahead += 2 * move
					steps++
				}
				if grid[ahead] == '#' {
					continue
				}
				robot = next
				grid[robot] = '.'
				ahead = robot + move
				for range steps {
					grid[ahead] = ']'
					grid[ahead-1] = '['
					ahead += 2 * move
				}
			case '>':
				steps := 0
				ahead := next
				for grid[ahead] == '[' {
					ahead += 2 * move
					steps++
				}
				if grid[ahead] == '#' {
					continue
				}
				robot = next
				grid[robot] = '.'
				ahead = robot + move
				for range steps {
					grid[ahead] = '['
					grid[ahead+1] = ']'
					ahead += 2 * move
				}
			default:
				valid := true
				var q []complex128
				if tile == '[' {
					q = []complex128{next}
				} else {
					q = []complex128{next - 1}
				}

				var i int
				for i < len(q) {
					lhs := q[i] + move
					rhs := q[i] + move + 1
					if grid[lhs] == '#' || grid[rhs] == '#' {
						valid = false
						break
					}
					if grid[lhs] == '[' {
						q = append(q, lhs)
					} else if grid[lhs] == ']' {
						q = append(q, lhs-1)
					}
					if grid[rhs] == '[' {
						q = append(q, rhs)
					}
					i++
				}
				if !valid {
					continue
				}
				robot = next
				grid[robot] = '.'
				for i := len(q) - 1; i > -1; i-- {
					pos := q[i]
					grid[pos] = '.'
					grid[pos+1] = '.'
					grid[pos+move] = '['
					grid[pos+move+1] = ']'
				}
			}
		}
	}

	display := false
	if display {
		for y := range 10 {
			for x := range 20 {
				pos := complex(float64(x), float64(y))
				if pos == robot {
					fmt.Print("@")
					continue
				}
				fmt.Print(string(grid[pos]))
			}
			fmt.Println()
		}
	}

	var total int
	for pos, tile := range grid {
		if tile != '[' {
			continue
		}
		x, y := int(real(pos)), int(imag(pos))
		total += x + y*100
	}
	return total
}

